public class MainActivity extends AppCompatActivity {

    MqttHelper mqttHelper;

    TextView dataTemperature;
    TextView dataHuminity;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        dataTemperature = findViewById(R.id.tvTemp);
        dataHuminity = findViewById(R.id.tvHum);

        startMqtt();
    }

    private void startMqtt() {
        mqttHelper = new MqttHelper(getApplicationContext());
        mqttHelper.setCallback(new MqttCallbackExtended() {
            @Override
            public void connectComplete(boolean b, String s) {

            }

            @Override
            public void connectionLost(Throwable throwable) {

            }

            @Override
            public void messageArrived(String topic, MqttMessage mqttMessage) throws Exception {
                Log.w("Debug", mqttMessage.toString());
                String[] results = cutResponseString(mqttMessage.toString());
                dataTemperature.setText(results[0] + " Â°C");
                dataHuminity.setText(results[1] + " %");
            }

            @Override
            public void deliveryComplete(IMqttDeliveryToken iMqttDeliveryToken) {

            }
        });
    }

    private String[] cutResponseString(String response) {
        return response.split(",");
    }
}
